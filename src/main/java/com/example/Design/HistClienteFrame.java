    private void avaliaButtonActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_avaliaButtonActionPerformed
        int row = histClienteLista.getSelectedRow();

        if ( row>= 0&&tableModel.isServicoTerminado(row)&&!tableModel.isAvaliou(row)) {
            dispose();
            Object aux = JOptionPane.showInputDialog(null, "Avalie o profissional " + tableModel.lista.get(row).getNomeProfissional(), null,
                    JOptionPane.PLAIN_MESSAGE, null, rating, 5);
            Double d;
            if (aux == null)
                d = 5.0;
            else
                d = Double.parseDouble(String.valueOf(aux));
            new Avaliacao(tableModel.getIdProfissional(row),
                    tableModel.getIdDoc(row), d);
        }
    }// GEN-LAST:event_avaliaButtonActionPerformed

