    public void getHistDaoCliente() {

        ApiFuture<QuerySnapshot> query = ConectaFirebase.bd.collection("histórico").get();

        try {
            for(DocumentSnapshot doc : query.get().getDocuments()){
                ArrayList aux = ((ArrayList)doc.get("informações"));
                if(aux.get(0).toString().equals(CurrentUser.uid))
                this.histCliente.add(new HistDaoCliente(aux.get(3).toString(), aux.get(5).toString(),
               aux.get(2).toString(),aux.get(12).toString(), (boolean)aux.get(9),(boolean)aux.get(11)));
            }
        } catch (Exception e) {
            //TODO: handle exception
        }

    }

