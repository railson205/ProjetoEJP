    private void logarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton1ActionPerformed
        if (senhaText.getPassword().length >= 6 && vemail.isVerified(emailText.getText())) {
            try {
                conecta.login(emailText.getText(), String.valueOf(senhaText.getPassword()));
                new HomeFrames().setVisible(true);
                dispose();
            } catch (Exception e) {
                System.out.println(e);
                // TODO: handle exception
            }
        } else {
            JOptionPane.showMessageDialog(null, "Senha ou Email incorretos");
        }
    }// GEN-LAST:event_jButton1ActionPerformed

